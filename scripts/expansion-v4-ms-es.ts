function ms(dom: string, std: number, seq: number, name: string, desc: string,
  method: string, area: string, stdCode: string, stdContent: string,
  cog: string, dMin: number, dMax: number, kw: string[]): ExpandedType {
  return {
    type_code: `MA-MS-${dom}-${String(std).padStart(3,'0')}-${String(seq).padStart(3,'0')}`,
    type_name: name, description: desc, solution_method: method,
    subject: '수학', area, standard_code: stdCode, standard_content: stdContent,
    cognitive: cog, difficulty_min: dMin, difficulty_max: dMax,
    keywords: kw, school_level: '중등', level_code: 'MS', domain_code: dom,
  };
}

